package com.example.demo.agroknow.cerebro.syngenta.varifield.enumerations;

public enum WheatVariety {

    GALLANT("Gallant"),
    REFLECTION("Reflection"),
    KWSSANTIAGO("KWSSantiago"),
    GRAHAM("Graham"),
    ;


    private String wheatVariety;

    WheatVariety(String wheatVariety) {
        this.wheatVariety = wheatVariety;
    }

    public String getWheatVariety() {
        return wheatVariety;
    }

    public void setWheatVariety(String wheatVariety) {
        this.wheatVariety = wheatVariety;
    }
}
























